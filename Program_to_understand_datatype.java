// program to understand datatypes in java
class Program_to_understand_datatype{
	public static void main(String args[]){
	// integer datatypes
	byte a = 10;
	short b = 213;
	int c = 3213;
	long d = 4352535l;
	System.out.println("The value of byte a is " + a);
	
	System.out.println("The value of short b is " + b);

	System.out.println("The value of int  c is " + c);

	System.out.println("The value of long  d is " + d);

	// floating data types
	float e = 4.5f;
	double f = 43.5646;


	System.out.println("The value of float e is " + e);
	System.out.println("The value of double f is " + f);
	
	// char
	char g = 'a';

	System.out.println("The value of char  g is " + g);
	// string
	String str = "string";

	System.out.println("The value of string str is " + str);
	
	//boolean
	boolean bool = (2>3);
	
	System.out.println("The value of boolean bool is " + bool);
	}
}
	
